function getId(y)
{
	if(y=="Management")
	{
		sessionStorage.id = 71;
	}
	else if(y=="Wireless")
	{
		sessionStorage.id = 72;
  	}
  	else if(y=="Forensics")
  	{
  		sessionStorage.id = 73;
  	}
  	else if(y=="Big Data")
  	{
  		sessionStorage.id = 74;
  	}
  	else
  	{
  		sessionStorage.id = 75;
  	}
}